/**
 * Render multiple platform icons with overflow indicator
 */
export function PlatformIcons({
  platforms,
  size = 16,
  maxVisible = 4,
  className = '',
}: {
  platforms: Platform[];
  size?: number;
  maxVisible?: number;
  className?: string;
}) {
  const visible = platforms.slice(0, maxVisible);
  const overflow = platforms.length - maxVisible;

  return (
    <div className={`flex items-center gap-1.5 ${className}`}>
      {visible.map((platform) => (
        <PlatformIcon key={platform} platform={platform} size={size} />
      ))}
      {overflow > 0 && (
        <span className="text-xs text-text-tertiary font-medium">+{overflow}</span>
      )}
    </div>
  );
}

